#int
dado = 10
print(type(dado))

#float
dadoF = 3.2
print(type(dadoF))

#String
dadoStr = "String"
print(type(dadoStr))

#boolean
dadoB = True
print(type(dadoB))

#list
dadoL = []
print(type(dadoL))

#tuple
dadoT = (22012005, "Alexandre", 19.8, "TDS")
print(type(dadoT))

#dicionrio
dadoD = {"Nome" : "Alexandre", "Idade": 19, "Curso": "TDS"}
print(type(dadoD))

#set
dadoSet = {"vdrty", 23, "hgty"}
